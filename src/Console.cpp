#include "Console.hpp"

#include <iostream>
#include <string>

//-~-~-~-~-~-~-~-~-~-~-~-~-~-~-~-~-~-~-~-~-~-~-~-~-~-~-~-~-~-~-~-~-~-~-~-~-~-~-~
Console::Console()
:   m_pScriptObject(nullptr)
,   m_pScriptEngine()
,   m_pScriptModule(nullptr)

{
    
    
}

//-~-~-~-~-~-~-~-~-~-~-~-~-~-~-~-~-~-~-~-~-~-~-~-~-~-~-~-~-~-~-~-~-~-~-~-~-~-~-~
Console::~Console()
{
    
}

//-~-~-~-~-~-~-~-~-~-~-~-~-~-~-~-~-~-~-~-~-~-~-~-~-~-~-~-~-~-~-~-~-~-~-~-~-~-~-~
static std::string sm_typeName("log");
//-~-~-~-~-~-~-~-~-~-~-~-~-~-~-~-~-~-~-~-~-~-~-~-~-~-~-~-~-~-~-~-~-~-~-~-~-~-~-~
const std::string&
Console::getScriptTypeName()
{
    return sm_typeName;    
}

//-~-~-~-~-~-~-~-~-~-~-~-~-~-~-~-~-~-~-~-~-~-~-~-~-~-~-~-~-~-~-~-~-~-~-~-~-~-~-~
Zen::Scripting::I_ScriptWrapper*
Console::getScriptObject()
{
    if (m_pScriptObject == nullptr)
    {
        m_pScriptObject = new ScriptWrapper_type(
            getScriptModule(),
            getScriptModule()->getScriptType(getScriptTypeName()),
            this->getSelfReference().get()
        );
    }

    return m_pScriptObject;
}

//-~-~-~-~-~-~-~-~-~-~-~-~-~-~-~-~-~-~-~-~-~-~-~-~-~-~-~-~-~-~-~-~-~-~-~-~-~-~-~
Console::pScriptModule_type
Console::getScriptModule()
{
    return m_pScriptModule->getScriptModule();
}

//-~-~-~-~-~-~-~-~-~-~-~-~-~-~-~-~-~-~-~-~-~-~-~-~-~-~-~-~-~-~-~-~-~-~-~-~-~-~-~
void
Console::registerScriptEngine(pScriptEngine_type _pScriptEngine)
{
    // Only do this once.
    assert(m_pScriptModule == nullptr);

    m_pScriptEngine = _pScriptEngine;

    static Zen::Scripting::script_module module(_pScriptEngine, "console");

    // Keep a reference to the module.
    m_pScriptModule = &module;

    // ScriptAction
    module.addType<Console>("log", "Log Object")
        .addMethod("print", &Console::print)
    ;

    module.activate();
}

//-~-~-~-~-~-~-~-~-~-~-~-~-~-~-~-~-~-~-~-~-~-~-~-~-~-~-~-~-~-~-~-~-~-~-~-~-~-~-~
std::shared_ptr<Console>
Console::getSelfReference()
{
    return shared_from_this();
}

//-~-~-~-~-~-~-~-~-~-~-~-~-~-~-~-~-~-~-~-~-~-~-~-~-~-~-~-~-~-~-~-~-~-~-~-~-~-~-~
void
Console::print(const std::string& _value)
{
    std::cout << _value << std::endl;
}



